// Helper functions for the dashboard

// Get total article count for the current year (2024)
export const getTotalArticlesForCurrentYear = () => {
  const currentYear = new Date().getFullYear();
  return articles.filter(article => {
    const articleYear = new Date(article.onlineDate).getFullYear();
    return articleYear === currentYear;
  }).length;
};

// Get approved articles count for the current year
export const getApprovedArticlesCount = () => {
  const currentYear = new Date().getFullYear();
  return articles.filter(article => {
    const articleYear = new Date(article.onlineDate).getFullYear();
    return articleYear === currentYear && article.articleStatus === "Approved";
  }).length;
};

// Get rejected articles count for the current year
export const getRejectedArticlesCount = () => {
  const currentYear = new Date().getFullYear();
  return articles.filter(article => {
    const articleYear = new Date(article.onlineDate).getFullYear();
    return articleYear === currentYear && article.articleStatus === "Rejected";
  }).length;
};

// Calculate total spend for 2024 (this is a placeholder since we don't have 2024 data)
// For the mock, we'll use the current articles and sum their prices
export const getTotalSpendFor2024 = () => {
  return articles.reduce((total, article) => {
    return total + article.customerApcListPrice;
  }, 0);
};

// Get articles by month
export const getArticlesByMonth = () => {
  const months = ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"];
  const articlesByMonth = months.map(month => {
    const approved = articles.filter(article => {
      const monthStr = article.allocationMonth.split('-')[0];
      return monthStr === month && article.articleStatus === "Approved";
    }).length;
    
    const rejected = articles.filter(article => {
      const monthStr = article.allocationMonth.split('-')[0];
      return monthStr === month && article.articleStatus === "Rejected";
    }).length;
    
    return {
      month,
      approved,
      rejected,
      total: approved + rejected
    };
  });
  
  return articlesByMonth;
};

// Get unique institutions
export const getUniqueInstitutions = () => {
  const institutions = [...new Set(articles.map(article => article.approvingInstitution))];
  return institutions;
};

// Get articles by institution
export const getArticlesByInstitution = () => {
  const institutions = getUniqueInstitutions();
  
  return institutions.map(institution => {
    const institutionArticles = articles.filter(article => article.approvingInstitution === institution);
    const approved = institutionArticles.filter(article => article.articleStatus === "Approved").length;
    const rejected = institutionArticles.filter(article => article.articleStatus === "Rejected").length;
    const total = approved + rejected;
    const spend = institutionArticles.reduce((sum, article) => sum + article.customerApcListPrice, 0);
    
    return {
      institution,
      approved,
      rejected,
      total,
      spend
    };
  });
};

